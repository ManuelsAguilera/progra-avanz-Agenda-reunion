    public int parseStringHour(String timeString) throws InvalidTimeException {
        //Prueba Exception
        try {
            String[] parts = timeString.split(":");
            if (parts.length == 2) {
                int hours = Integer.parseInt(parts[0]);
                int minutes = Integer.parseInt(parts[1]);

                // Verificar si las horas y los minutos son v√°lidos
                if (hours >= 0 && hours <= 23 && minutes >= 0 && minutes <= 59) {
                    return hours * 100 + minutes;
                } else {
                    throw new InvalidTimeException("Hora o minutos inv√°lidos");
                }
            }
        } catch (NumberFormatException e) {
            throw new InvalidTimeException("Formato de tiempo inv√°lido");
        }

        throw new InvalidTimeException("Formato de tiempo inv√°lido");
    }
    
